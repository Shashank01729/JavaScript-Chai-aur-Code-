// while Loop
let index=0;
while(index<=10){
    console.log(`Value of index is ${index}`);
    index+=2;
}

// whle loop in arrays
let MC=["Ayanokouji", "Hachiman", "Yuji"]
let arr=0;
while(arr<MC.length){
    console.log(`Value is ${MC[arr]}`);
    arr++;
}


